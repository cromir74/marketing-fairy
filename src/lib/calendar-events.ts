export function getSeason(month: number): string {
    if (month >= 2 && month <= 4) return '봄';
    if (month >= 5 && month <= 7) return '여름';
    if (month >= 8 && month <= 10) return '가을';
    return '겨울';
}

export function getMonthPhase(day: number): string {
    if (day <= 10) return '초';
    if (day <= 20) return '중';
    return '말';
}
